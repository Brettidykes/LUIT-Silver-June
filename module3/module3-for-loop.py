# Iterating over each character in the string 'hello!' and assigning it to the variable 'letter'
for letter in 'hello!':  
    # Printing the current character being processed
    print('Current letter:', letter)

# Looping over a range of numbers from 1 to 10 (range is inclusive of 1, exclusive of 11)
for counter in range(1, 11):  
    # Printing the current number in the range
    print(counter)

# This line is executed after the loop finishes, signaling the end of the program
print('Finished!')
